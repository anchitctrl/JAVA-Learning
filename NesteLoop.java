package learnJava;

public class nestedLoop {
	public static void main(String[] args) {
		
//	* * * *
//	* * * *
//	* * * *
//	* * * *
//	Lets try to print this or other patterns
		
		
		for(int j=1; j<=4;j++) {
			for(int i=1; i<=4;i++) {
				System.out.print(i+ " " );
			}
			System.out.println();
		}
	}

}
